<h2>About Us</h2>

<p class="intro">Hello and welcome to Good Baad. This is a brief overview of who we are, what we do and where we are going. To keep up to date with the most exciting stats and recent developments, visit the Good Baad blog and podcast.</p>

<h3>The Site Content:</h3>

<p class="intro">Good Baad aims to be the definitive record of public opinion!</p>

<p>We hope to present the statistics that interest you in the most simple, uncluttered manner possible. By breaking opinions down into the most black and white terms - <strong>good</strong> and <strong>baad</strong>. It is hoped that you will see them as a good starting point for thought and debate.</p>

<p>It's all about strong opinions; sitting on the fence is nowhere near as interesting. "Marks out of 5" works well for talent shows, but polar points of view make for more interesting discussions, so join in.</p>

<p>We hope these stats will act as a useful basis for blog posts, articles and debates. When using this site as a source please link back to us - the more users we have the more useful the information becomes. We plan to add widgets to make adding our features to your sites and social networks easier.</p>

<p>Good Baad would like to thank <a href="http://www.freebase.com/" target="blank">Freebase</a>, <a href="http://code.google.com/apis/chart/" target="blank">Google Charts</a> and <a href="http://www.daylife.com/" target="blank">DayLife</a> for providing the topic data, pie charts and news data, respectively.</p>

<p>And remember, if there is a topic you can't find, add it to <a href="http://www.freebase.com/" target="blank">FreeBase</a> and you can then post your opinion on it here.</p>

<h3>Themes</h3>

<p>We have chosen our sheep based theme due to the availability of the domain name (baad sounds a bit like a sheep doesn't it?). It also seemed to make (a little) sense that users would be part of a "flock" who share certain opinions.</p>

<p>We've also kept the colour scheme to a minimum to emphasise the "black and white" nature of the site. Although this may change as the site develops.<p>

<h3>About The Developer</h3>

<p>The site was conceived and built by me (Joel Hughes, a web developer from Bristol, UK), as an evenings and weekends project.</p>

<p>So far the site is a challenge to see what can be done with a simple concept, spare time and no budget whatsoever!</p>

<p>The project started to take shape in June 2008, and is still very much a work in progress. The bulk of development has been a super-light-weight framework that should (hopefully) keep the server costs, and therefore environmental damage, to a minimum.</p>

